>> O usuário abre o computador, vê a tela e solicita uma informação.
   o Front end, faz uma requisição ao Back end, ai a API analiza a solicitação, identifica
   e faz a requisição ao Banco de dados, depois com os dados, a API organiza e manda em
   formato JASON, que em uma tela preta para o Front end, que por sua vez organiza e deixa 
   tudo tranquilo para a entrega, atendendo a solicitação do usuário.


>> O usuário faz uma Request e a API faz uma Response.