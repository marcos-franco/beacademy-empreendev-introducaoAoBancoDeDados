>> A estrutura básica de um banco de dados é composta por tabelas, campos e relacionamentos.

>> Tabelas: 
   As tabelas são as estruturas básicas para armazenar dados no banco de dados. 
   Cada tabela representa uma entidade específica, como clientes, produtos, pedidos, etc. 
   Cada linha de uma tabela representa um registro ou uma ocorrência dessa entidade.

>> Campos: 
   Os campos são as colunas das tabelas que representam os atributos dos registros.
   Exemplo: Em uma tabela de clientes, os campos podem ser nome, endereço, e-mail, etc.
   Cada campo tem um tipo de dado específico, como texto, número, data, etc.

>> Relacionamentos: 
   Os relacionamentos são os laços entre as tabelas que permite que os dados sejam relacionados entre si.
   Exemplo: Uma tabela de pedidos pode estar relacionada a uma tabela de clientes, permitindo que você saiba quem fez 
   cada pedido. Existem diferentes tipos de relacionamentos, como um-para-um, um-para-muitos e muitos-para-muitos.

>> Além desses elementos básicos, os bancos de dados também incluem recursos adicionais, como índices, chaves 
   primárias e estrangeiras, visões e procedimentos armazenados. Esses recursos permitem que os dados sejam gerenciados
   de forma mais eficiente e segura.
