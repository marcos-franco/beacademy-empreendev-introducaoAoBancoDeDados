>> Um banco de dados é uma coleção de dados logicamente relacionados, com algum significado.
   Implica que, associações aleatórias de dados não possam ser chamadas de bases de dados. 
   É projetado, construido e preenchido com dados para um propósito especifico. possui um grupo
   de usuários e algumas aplicações concebidas para atender a esses usuarios. Implica que a representação
   abstrata de uma realidade em um sistema eletronico. Com esta representacao, podemos gerar instancias, 
   ou seja, dados concretos provenientes de uma realidade.


>> Um banco de dados é uma coleção organizada de informações, ou dados estruturados, normalmente armazenados
   eletronicamente em um sistema de computador, que permite a consulta e a atualização de dados pelo usuário.

>> Dado, é o valor do campo, quando é armazenado no banco de dados.